package com.picmap.app.heartComments;

public class HeartCommentsDTO {
	private Long heartCommentNum;
	private Long commentNum;
	private Long memberNum;

	public Long getHeartCommentNum() {
		return heartCommentNum;
	}

	public void setHeartCommentNum(Long heartCommentNum) {
		this.heartCommentNum = heartCommentNum;
	}

	public Long getCommentNum() {
		return commentNum;
	}

	public void setCommentNum(Long commentNum) {
		this.commentNum = commentNum;
	}

	public Long getMemberNum() {
		return memberNum;
	}

	public void setMemberNum(Long memberNum) {
		this.memberNum = memberNum;
	}

}
